    public void loadUser() {

        ResultSet resultSet = user.readUser();

        DefaultTableModel model = (DefaultTableModel) tableUser.getModel();
        model.setRowCount(0);

        try {
            while (resultSet.next()) {
                int userId = resultSet.getInt("user_id");
                String username = resultSet.getString("username");
                String password = resultSet.getString("password");
                String email = resultSet.getString("email");
                String phoneNo = resultSet.getString("phoneNo");
                model.addRow(new Object[]{userId, username, password, email, phoneNo});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e, "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }

    public void loadCustomer() {

        ResultSet resultSet = customer.readCustomer();

        DefaultTableModel model = (DefaultTableModel) tableCustomer.getModel();
        model.setRowCount(0);

        try {
            while (resultSet.next()) {
                int customerId = resultSet.getInt("customer_id");
                String customername = resultSet.getString("name");
                String customerEmail = resultSet.getString("email");
                String customerPhoneNo = resultSet.getString("phoneNo");
                model.addRow(new Object[]{customerId, customername, customerEmail, customerPhoneNo});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e, "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }

